void PhysicsPlayground::KeyboardHold()
{
	auto& player = ECS::GetComponent<PhysicsBody>(MainEntities::MainPlayer());
	auto& box = ECS::GetComponent<PhysicsBody>(scaleBox1);
	auto& boxSprite = ECS::GetComponent<Sprite>(scaleBox1);
	auto& penta = ECS::GetComponent<PhysicsBody>(scalePenta1);
	auto& pentaSprite = ECS::GetComponent<Sprite>(scalePenta1);
	
	float speed = 1.f;
	b2Vec2 vel = b2Vec2(0.f, 0.f);

	if (Input::GetKey(Key::Shift))
	{
		speed *= 5.f;
	}

	if (Input::GetKey(Key::A))
	{
		player.GetBody()->ApplyForceToCenter(b2Vec2(-400000.f * speed , 0.f), true);
	}
	if (Input::GetKey(Key::D))
	{
		player.GetBody()->ApplyForceToCenter(b2Vec2(400000.f * speed , 0.f), true);
	}

	//Change physics body size for circle
	if (Input::GetKey(Key::O))
	{
		//player.ScaleBody(1.3 * Timer::deltaTime, 0);
		if (sca < 40)
		{


			box.ScaleBody(0.7f, 0);
			boxSprite.SetHeight(20 + sca);
			boxSprite.SetWidth(20 + sca);

			penta.ScaleBody(0.7f, 0);
			pentaSprite.SetHeight(20 + sca);
			pentaSprite.SetWidth(20 + sca);
			sca += 1;
		}
		std::cout<<"\n" << sca;
	}
	else if (Input::GetKey(Key::I))
	{
		if (sca > 5)
		{
			//player.ScaleBody(-1.3 * Timer::deltaTime, 0);
			box.ScaleBody(-0.7f, 0);
			boxSprite.SetHeight(20 + sca);
			boxSprite.SetWidth(20 + sca);


			penta.ScaleBody(-0.7f, 0);
			pentaSprite.SetHeight(20 + sca);
			pentaSprite.SetWidth(20+ sca);
			sca -= 1;
		}

		std::cout << "\n" << sca;
	}
}

void PhysicsPlayground::KeyboardDown()
{
	auto& player = ECS::GetComponent<PhysicsBody>(MainEntities::MainPlayer());
	auto& canJump = ECS::GetComponent<CanJump>(MainEntities::MainPlayer());

	if (Input::GetKeyDown(Key::T))
	{
		PhysicsBody::SetDraw(!PhysicsBody::GetDraw());
	}
	if (canJump.m_canJump)
	{
		if (Input::GetKeyDown(Key::Space))
		{
			player.GetBody()->ApplyLinearImpulseToCenter(b2Vec2(0.f, 200000.f), true);
			canJump.m_canJump = false;
		}
	}
}

